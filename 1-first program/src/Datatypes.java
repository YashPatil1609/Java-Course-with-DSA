public class Datatypes {
    public static void main(String[] args) {

        int a = 10;

        float b = 20.11313f;     // at the end we always has to write f at the end of decimal precision, float takes 5 decimal places

        char c = 'c';

        double d = 7139.71371313;

        long l = 8716196931L;   // at end always write L for specifying long type

        boolean bool = false;


    }
}
